// A C++ program to show operations between two points

#include<iostream>
using namespace std;

int main()
{
  int a,b;
  cout<<"Enter the first number: ";
  cin>>a;
  cout<<"Enter the second number: ";
  cin>>b;

  cout<<"The sum of the two number is: "<<a+b<<endl;
  cout<<"The difference between two  number is: "<<a-b<<endl;
  cout<<"Product of two number is: "<<a*b<<endl;
  cout<<"Quoteint when a divided by b is: "<<a/b<<endl;
  cout<<"Remainder when a is divided by b is: "<<a%b<<endl;

  return 0;
}
